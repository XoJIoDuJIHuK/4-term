use UNIVER;
select * from GROUPS full join STUDENT
	on GROUPS.IDGROUP = STUDENT.IDGROUP
	order by STUDENT.IDGROUP
	
select count(*) from GROUPS full join STUDENT
	on GROUPS.IDGROUP = STUDENT.IDGROUP
	where STUDENT.IDGROUP is null
